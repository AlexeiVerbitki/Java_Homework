class myFirstApp {
    public static void main(String[] args) {
        int x = 1;
        System.out.println("X = 1 so This is  before the Loop");
        while (x < 4) {
            System.out.println("The Value of x = " + x );
            System.out.println("   x < 4 So this is in the Loop");

            x=x+1;
        }
        if (x == 4){
            System.out.println(" x = 4 So Loop Ended");
        }

        System.out.println();
        System.out.println("I Rule ");
        System.out.println("The World ");
    }

}


