package Java_How_to_Programm_Early_Objects_Paul_Deitel.Chapter_7_Arrays;

 public class Asterics {
    int counter;

     public static void Counter (int counter) {
         System.out.print("\t");
         for (int i = 1; i <= counter; i++) {
             System.out.print("*");
         }
         System.out.print("\n");
     }

}


